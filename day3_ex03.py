# Concatenate the string "I am " and the integer 29 to produce a string which reads "I am 29".
# Remember that we can only concatenate strings to other strings, so you will have to convert
# the integer to a string before you can perform the concatenation.

print("I am " + str(29))

print("I am", str(29))

age = str(29)
print(f"I am {age}")
