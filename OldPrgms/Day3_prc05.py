# Basic String Processing : four important options: lower, upper, capitalize, and title.
# In order to use these methods, we just need to use the dot notation again, just like with format.


print("Hello, World!".lower())             # "hello, world!"
print("Hello, World!".upper())             # "HELLO, WORLD!"
print("Hello, World!".capitalize())        # "Hello, world!" 
print("Hello, World!".title())             # "Hello, World!"


# Use the strip method remto remove white space from the ends of a string

print("  Hello, World!  ".strip())             # "Hello, World!"

